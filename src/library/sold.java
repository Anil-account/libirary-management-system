package library;

public class sold {
	String book_name, pub_name, sold_date;
	int id, quantity;
	sold(int id, String book_name, String pub_name, String sold_date, int quantity) {
		  this.id = id;
		  this.book_name = book_name;
		  this.pub_name = pub_name;
		  this.sold_date = sold_date;
		  this.quantity = quantity;
	}
}